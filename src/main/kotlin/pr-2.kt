//.2 Type conversion:
//Integer to Double, String to Integer, String to Double.
fun main(){
    println("Yesha Chauhan")
    println("INT to Double")
    var a:Int=10
    var b:Double = a.toDouble()
    println(a)
    println(b)

    println("String To Int")
    var c:String="10"
    var d:Int=c.toInt()
    println(c)
    println(d)

    println("String to Double")
    var e:String="10.2"
    var f:Double=e.toDouble()
    println(e)
    println(f)
}